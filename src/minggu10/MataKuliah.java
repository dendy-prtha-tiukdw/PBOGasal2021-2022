package minggu10;

public class MataKuliah {
    private int sks;
    private int syaratSKS;
    public MataKuliah(int sks,int syaratSKS) {
        this.sks = sks;
        this.syaratSKS = syaratSKS;
    }

    public int getSks() {
        return sks;
    }

    public int getSyaratSKS() {
        return syaratSKS;
    }
}
